# ──────────────────────────────────────────────────────────────────────────
# Header builder — bg styles are inlined directly so Gmail / Outlook
# can't strip them, and the dark color is preserved by Gmail dark mode.
# ──────────────────────────────────────────────────────────────────────────
def header(
    title: str,
    subtitle: str | None = None,
    badge: str | None = None,
    language: str = "ar",
) -> str:
    """Build a branded header with the inline PNG wordmark and title."""
    font_family = FONT_AR if language == "ar" else FONT_LATIN

    badge_html = (
        f'<div style="margin-top:14px;">'
        f'<span style="display:inline-block;background:rgba(212,175,55,0.2);color:{GOLD};'
        f"border:1px solid rgba(212,175,55,0.45);font-size:12px;padding:5px 14px;"
        f"border-radius:999px;font-weight:600;letter-spacing:0.5px;"
        f'font-family:{FONT_LATIN};direction:ltr;">{badge}</span></div>'
        if badge
        else ""
    )

    sub_html = (
        f'<p style="color:#cdd2dc;margin:0;font-size:14px;font-family:{font_family};">{subtitle}</p>'
        if subtitle
        else ""
    )

    # Inline background — three layered radial gradients on top of the
    # solid hero color, plus the dot grid. The solid color is the
    # guaranteed Outlook fallback.
    header_bg_style = (
        f"background-color:{HERO_BG};"
        "background-image:"
        "radial-gradient(circle at 12% 8%, rgba(59,130,246,0.55) 0%, rgba(59,130,246,0.18) 28%, transparent 58%),"
        "radial-gradient(circle at 88% 92%, rgba(168,85,247,0.32) 0%, rgba(168,85,247,0.10) 35%, transparent 65%),"
        "radial-gradient(rgba(255,255,255,0.06) 1px, transparent 1px);"
        "background-size:100% 100%, 100% 100%, 26px 26px;"
        "background-position:0 0, 0 0, 0 0;"
        "background-repeat:no-repeat, no-repeat, repeat;"
    )

    # Gmail mobile auto-inverts elements with light text on a CSS-only
    # dark background. The only reliable way to keep a dark hero is the
    # legacy `<table bgcolor="...">` attribute — Gmail respects it and
    # leaves the background alone. We then layer the radial gradients on
    # top via inline `style` so capable clients (Apple Mail, iOS Mail,
    # Yahoo) still get the glows.
    return f"""
    <!--[if mso]>
    <v:rect xmlns:v="urn:schemas-microsoft-com:vml" fill="true" stroke="false" style="mso-width-percent:1000;height:300px;">
      <v:fill type="solid" color="{HERO_BG}"/>
      <v:textbox inset="0,0,0,0">
    <![endif]-->
    <table role="presentation" width="100%" cellpadding="0" cellspacing="0" border="0" bgcolor="{HERO_BG}" class="gmail-fix" style="background-color:{HERO_BG};">
      <tr>
        <td bgcolor="{HERO_BG}" align="center" valign="middle" class="gmail-fix" style="{header_bg_style}padding:48px 28px 40px;text-align:center;color:#ffffff;">
          {_logo_block(language)}
          <h1 style="color:#ffffff;margin:24px 0 8px;font-size:24px;font-weight:700;font-family:{font_family};line-height:1.4;mso-line-height-rule:exactly;">{title}</h1>
          {sub_html}
          {badge_html}
        </td>
      </tr>
      <tr>
        <td bgcolor="{HERO_BG}" height="3" style="background-color:{HERO_BG};line-height:3px;font-size:0;">
          <table role="presentation" width="100%" cellpadding="0" cellspacing="0" border="0">
            <tr>
              <td height="3" style="height:3px;line-height:3px;font-size:0;background-color:{GOLD};">&nbsp;</td>
            </tr>
          </table>
        </td>
      </tr>
    </table>
    <!--[if mso]>
      </v:textbox>
    </v:rect>
    <![endif]-->
    """


# ──────────────────────────────────────────────────────────────────────────
# Wrapper — call this from every template
# ──────────────────────────────────────────────────────────────────────────
def wrap(inner_html: str, language: str = "ar", preheader: str | None = None) -> str:
    """Wrap rendered template body in a full HTML document with brand chrome.

    Args:
        inner_html: The template body (header + body sections).
        language: 'ar' (default, Egyptian Arabic, RTL) or 'en'.
        preheader: Optional inbox preview text.
    """
    is_ar = language == "ar"
    direction = "rtl" if is_ar else "ltr"
    preheader_html = (
        f'<div style="display:none;max-height:0;overflow:hidden;opacity:0;font-size:1px;line-height:1px;color:transparent;">{preheader}</div>'
        if preheader
        else ""
    )
    return f"""<!DOCTYPE html>
<html lang="{language}" dir="{direction}">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<meta name="x-apple-disable-message-reformatting">
<meta name="color-scheme" content="only light">
<meta name="supported-color-schemes" content="only light">
<meta http-equiv="X-UA-Compatible" content="IE=edge">
<title>NUMU</title>
{_styles(language)}
<style type="text/css">
  /* Force light color scheme — stops Gmail / Outlook.com mobile from
     auto-inverting our dark hero header into a light one. */
  :root {{ color-scheme: only light; supported-color-schemes: only light; }}
  /* Gmail-specific dark-mode override: force the header cell to keep
     its dark background and white text even when the user is in dark
     mode. The [data-ogsc] selector targets Outlook.com Gmail-style
     rendering. */
  u + .body .gmail-fix {{ background-color: {HERO_BG} !important; color: #ffffff !important; }}
  [data-ogsc] .gmail-fix {{ background-color: {HERO_BG} !important; color: #ffffff !important; }}
  @media (prefers-color-scheme: dark) {{
    .gmail-fix {{ background-color: {HERO_BG} !important; color: #ffffff !important; }}
  }}
</style>
<!--[if mso]>
<style type="text/css">
  body, table, td, div, p, a {{ font-family: Tahoma, Arial, sans-serif !important; }}
</style>
<![endif]-->
</head>
<body class="body" style="margin:0;padding:0;background:{CREAM};direction:{direction};width:100%;-webkit-text-size-adjust:100%;-ms-text-size-adjust:100%;">
{preheader_html}
<!--[if mso]>
<table role="presentation" width="100%" cellpadding="0" cellspacing="0" border="0" style="background:{CREAM};"><tr><td align="center" style="padding:32px 16px;">
<table role="presentation" width="600" cellpadding="0" cellspacing="0" border="0" style="background:#ffffff;"><tr><td>
<![endif]-->
<table role="presentation" width="100%" cellpadding="0" cellspacing="0" border="0" style="background:{CREAM};">
  <tr>
    <td align="center" style="padding:32px 16px;">
      <table role="presentation" width="600" cellpadding="0" cellspacing="0" border="0" class="wrapper" style="max-width:600px;width:100%;margin:0 auto;background:#ffffff;border-radius:16px;overflow:hidden;box-shadow:0 4px 32px rgba(10,26,61,0.08);">
        <tr>
          <td>
            {inner_html}
            {_footer(language)}
          </td>
        </tr>
      </table>
    </td>
  </tr>
</table>
<!--[if mso]>
</td></tr></table>
</td></tr></table>
<![endif]-->
</body>
</html>"""
