FONT_LATIN = "'Alexandria', 'Inter', 'Segoe UI', Arial, sans-serif"
FONT_BRAND_AR = "'Aref Ruqaa', 'Traditional Arabic', serif"

# NUMU brand wordmark — نُمو set in Aref Ruqaa Bold, white on transparent.
# Generated by scripts/generate_wordmark.py via headless Chromium so the
# calligraphic shaping (cascading noon→meem→waw + dhamma) is correct.
# WORDMARK_WIDTH/HEIGHT come from the generator's sidecar file.
from src.infrastructure.external_services.resend.email_templates._assets.wordmark import (  # noqa: E402
    WORDMARK_HEIGHT,
    WORDMARK_WIDTH,
)


def _wordmark_url() -> str:
    """Return the absolute URL for the white wordmark PNG.

    Reads from settings.brand_assets_base_url so dev/staging/prod can
    each override the host. Defaults to the landing-page asset which
    is already served by the production nginx config.
    """
    try:
        from src.config import settings

        base = settings.brand_assets_base_url.rstrip("/")
    except Exception:
        base = "https://numueg.app"
    return f"{base}/numu-logo-white.png"


# ──────────────────────────────────────────────────────────────────────────
# Logo block (email-safe — uses inline base64 PNG so the Aref Ruqaa
# rendering is preserved across every email client, even when Google
# Fonts are blocked or stripped).
# ──────────────────────────────────────────────────────────────────────────
def _logo_block(language: str = "ar") -> str:
    """Render the official NUMU brand logo as a hosted PNG image.

    Uses an absolute URL (not a data URL) because Gmail strips base64
    data: URIs from <img src>. The image is the same Arabic wordmark
    shipped with the landing page, recolored white for the dark email
    header.
    """
    # Display height in px. The Aref Ruqaa wordmark is roughly 0.75 wide
    # vs tall (the dhamma sits above the noon, the waw descends below),
    # so we anchor on height and let the width fall out.
    display_h = 140
    display_w = max(1, round(WORDMARK_WIDTH * display_h / WORDMARK_HEIGHT))
    url = _wordmark_url()
    return f"""
    <table role="presentation" cellpadding="0" cellspacing="0" border="0" align="center" style="margin:0 auto;">
      <tr>
        <td align="center" style="text-align:center;">
          <img src="{url}"
               width="{display_w}" height="{display_h}"
               alt="NUMU"
               style="display:block;width:{display_w}px;height:{display_h}px;max-width:{display_w}px;border:0;outline:none;text-decoration:none;-ms-interpolation-mode:bicubic;">
        </td>
      </tr>
    </table>
    """


